"""" Нарисовать в консоли ёлку спросив
у пользователя количество рядов.
Пример результата:
Сколько рядов у ёлки? 5
*
***
*****
*******
********* """

# Запрашиваем у пользователя количество рядов для ёлки
rows = int(input("Сколько рядов у ёлки? "))

# Рисуем ёлку
for i in range(1, rows + 1):
    # Формируем строку с звездочками и центрируем её
    stars = "*" * (2 * i - 1)
    centered_stars = stars.center(2 * rows - 1)
    print(centered_stars)



